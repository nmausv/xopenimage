/* CIEXYZ <-> CIELAB */
/*********************/

/* technically L*a*b* 'L-star', 'a-star', 'b-star' */
/* for CIELAB (vs Hunter Lab) */

/* D65 white point for XYZ = {0.9505, 1, 1.0890} */

/* flab() */
/* conditional function for Lab */
static double
flab(
 double x)
{
double ret;

  if (x > 0.008856) {
    ret = pow(x, (1.0 / 3.0) );
  } else {
    ret = (x * (841.0 / 108.0) + (4.0 / 29.0));
  }
  return(ret);
}

/* filab() */
/* inverse of conditional function for Lab */
static double
filab(
 double x)
{
double ret;

  if (x > (6.0 / 29.0)) {
    ret = pow(x, 3.0);
  } else {
    ret = (x - (4.0 / 29.0)) * (108.0 / 841.0);
  }
  return(ret);
}


/* XYZ2Lab() */
void
XYZ2Lab(
 double X,
 double Y,
 double Z,
 double *L,
 double *a,
 double *b)
{
double fy;

  if (Y > 0.008856) {
    fy = pow(Y, (1.0 / 3.0) );
    *L = 116.0 * fy - 16.0;
  } else {
    *L = (24389.0 / 27.0) * Y;
    /* 24389 = 29^3 */
    fy = (*L + 16.0) / 116.0;
  }

  *a = 500.0 * ( flab(X / 0.9505) - fy);
  *b = 200.0 * ( fy - flab(Z / 1.0890));

}


/* Lab2XYZ() */
void
Lab2XYZ(
 double L,
 double a,
 double b,
 double *X,
 double *Y,
 double *Z)
{
double Li;

  Li = (L + 16.0)/116.0;
  *Y = filab(Li);
  *X = 0.9505 * filab(Li + a / 500.0);
  *Z = 1.0890 * filab(Li - b / 200.0);  
}



