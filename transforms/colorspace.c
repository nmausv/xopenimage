/*******************/
/* CIEXYZ <-> sRGB */
/*******************/

/**************/
/* sRGB2XYZ() */
/**************/
/* sRGB to CIEXYZ */
/*  assumes sRGB has been linearized */
/*  reference: IEC (fee) */
/*   https://www.w3.org/TR/css-color-4/ */
/*  X = R * (506752 / 1228815)  + G * ( 87881 / 245763)  + B * (  12673 /   70218); */
/*  Y = R * ( 87098 /  409605)  + G * (175762 / 245763)  + B * (  12673 /  175545); */
/*  Z = R * (  7918 /  409605)  + G * ( 87881 / 737289)  + B * (1001167 / 1053270); */
void
sRGB2XYZ(
 double sR,
 double sG,
 double sB,
 double *X,
 double *Y,
 double *Z)
{
  if (sR > 1.0 || sR < 0.0 ||
      sG > 1.0 || sG < 0.0 ||
      sB > 1.0 || sB < 0.0) {
    fprintf(stderr, "sRGB2XYZ: RGB out of range\n");
  }

  *X = sR * (0.4124)  + sG * (0.3576)  + sB * (0.1805);
  *Y = sR * (0.2126)  + sG * (0.7152)  + sB * (0.0722);
  *Z = sR * (0.0193)  + sG * (0.1192)  + sB * (0.9505);

}

/**************/
/* XYZ2sRGB() */
/**************/
/* XYZ tristimulus to sRGB tristimulus/linear */
/*  IEC 61966-2-1 */
/*  www.color.org/sRGB.pdf */
/*  https://www.w3.org/TR/css-color-4/ */
/* R = X * (  12831 /   3959)  + Y * (   -329 /    214)  + Z * (-1974 /   3959); */
/* G = X * (-851781 / 878810)  + Y * (1648619 / 878810)  + Z * (36519 / 878810); */
/* B = X * (    705 /  12673)  + Y * (  -2585 /  12673)  + Z * (  705 /    667); */
void
XYZ2sRGB(
 double X,
 double Y,
 double Z,
 double *sR,
 double *sG,
 double *sB)
{
double lsR, lsG, lsB;

  lsR = X * ( 3.2406255)  + Y * (-1.537208 )  + Z * (-0.4986286);
  lsG = X * (-0.9689307)  + Y * ( 1.8757561)  + Z * ( 0.0415175);
  lsB = X * ( 0.0557101)  + Y * (-0.2040211)  + Z * ( 1.0569959);

  /* constrain to range 0.0 to 1.0 */
  if (lsR <= 0.0) *sR = 0.0;
  else if (lsR >= 1.0) *sR = 1.0;
  else *sR = lsR;

  if (lsG <= 0.0) *sG = 0.0;
  else if (lsG >= 1.0) *sG = 1.0;
  else *sG = lsG;

  if (lsB <= 0.0) *sB = 0.0;
  else if (lsB >= 1.0) *sB = 1.0;
  else *sB = lsB;

}


/***********************/
/* CIEXYZ <-> AdobeRGB */
/***********************/

/**************/
/* aRGB2XYZ() */
/**************/
/* Adobe RGB "tristimulus" to XYZ: matrix */
/*  these matrix values are as specified in */
/*  Adobe RGB Color Image Encoding, 2005, section 4.3.5.3 (page 13) */
void
aRGB2XYZ(
 double aR,
 double aG,
 double aB,
 double *X,
 double *Y,
 double *Z)
{
  if (aR > 1.0 || aR < 0.0 ||
      aG > 1.0 || aG < 0.0 ||
      aB > 1.0 || aB < 0.0) {
    fprintf(stderr, "aRGB2XYZ: RGB out of range\n");
  }

  *X = aR * (0.57667)  + aG * (0.18556)  + aB * (0.18823);
  *Y = aR * (0.29734)  + aG * (0.62736)  + aB * (0.07529);
  *Z = aR * (0.02703)  + aG * (0.07069)  + aB * (0.99134);
}


/**************/
/* XYZ2aRGB() */
/**************/
/* XYZ tristimulus to AdobeRGB (linear) */
/*  reference: Adobe RGB(1998) Color Image Encoding, page 11 */ 
void
XYZ2aRGB(
 double X,
 double Y,
 double Z,
 double *aR,
 double *aG,
 double *aB)
{
double laR, laG, laB;

  laR = X * ( 2.04159)  + Y * (-0.56501)  + Z * (-0.34473);
  laG = X * (-0.96924)  + Y * ( 1.87597)  + Z * ( 0.04156);
  laB = X * ( 0.01344)  + Y * (-0.11836)  + Z * ( 1.01517);

  /* constrain to range 0.0 to 1.0 */
  if (laR <= 0.0) *aR = 0.0;
  else if (laR >= 1.0) *aR = 1.0;
  else *aR = laR;

  if (laG <= 0.0) *aG = 0.0;
  else if (laG >= 1.0) *aG = 1.0;
  else *aG = laG;

  if (laB <= 0.0) *aB = 0.0;
  else if (laB >= 1.0) *aB = 1.0;
  else *aB = laB;

}


/********************/
/* AdobeRGB -> sRGB */
/********************/
/* from wider gamut to standard gamut */
/*  from smaller to wider doesn't make much sense */

/************/
/* a2sRGB() */
/************/
/* (linearized) AdobeRGB to (linearized) sRGB */ 
/*  matrix values derived from matrix multiplication */
/*  of sRGB = (XYZ2sRGB) * (aRGB2XYZ) * aRGB  */
/*  output will be between 0.0 to 1.0 */
void
a2sRGB(
 double aR,
 double aG,
 double aB,
 double *sR,
 double *sG,
 double *sB)
{
double lsR, lsG, lsB;

  /* warn */
  if (aR > 1.0 || aR < 0.0 ||
      aG > 1.0 || aG < 0.0 ||
      aB > 1.0 || aB < 0.0) {
    fprintf(stderr, "a2sRGB: input AdobeRGB out of range\n");
  }

  lsR = aR * (1.39822     )  + aG * (-0.3983   )  + aB * (-0.0000639288 );
  lsG = aR * (0.00010627  )  + aG * ( 0.999914 )  + aB * ( 0.00000180956);
  lsB = aR * (0.0000333087)  + aG * (-0.0429381)  + aB * ( 1.04297      );

  /* constrain to range 0.0 to 1.0 */
  if (lsR <= 0.0) *sR = 0.0;
  else if (lsR >= 1.0) *sR = 1.0;
  else *sR = lsR;

  if (lsG <= 0.0) *sG = 0.0;
  else if (lsG >= 1.0) *sG = 1.0;
  else *sG = lsG;

  if (lsB <= 0.0) *sB = 0.0;
  else if (lsB >= 1.0) *sB = 1.0;
  else *sB = lsB;

}


