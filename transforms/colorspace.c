/**************/
/* lin2sRGB() */
/**************/
/* convert from linear sRGB to component sRGB */
/*  output will be between 0.0 to 1.0 */
/*  reference: www.color.org/sRGB.pdf */
double
lin2sRGB(
 double inLin)
{
double ret;

  if (inLin <= 0.0) {
    ret = 0.0;
  } else if (inLin >= 1.0) {
    ret = 1.0;
  } else if (inLin <= 0.00313066844250063L) {
    ret = inLin * 12.92L;
  } else {
    ret = pow(inLin, 1.0L / 2.4L) * 1.055L - 0.055L;
  }
  return(ret);
}

/**************/
/* lin2aRGB() */
/**************/
/* convert from linear AdobeRGB to component AdobeRGB [0...1] */
/*  output will be between 0.0 to 1.0 */
/* reference: "Adobe RGB (1998) Color Image Encoding" */
double
lin2aRGB(
 double inLin)
{
double ret;

  if (inLin <= 0.0) {
    ret = 0.0;
  } else if (inLin >= 1.0) {
    ret = 1.0;
  } else {
    ret = pow(inLin, 256.0L / 563.0L);
  }
  return(ret);
}


